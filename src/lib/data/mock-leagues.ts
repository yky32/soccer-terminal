function hoursFromNow(hours: number) {
  return new Date(Date.now() + hours * 3_600_000).toISOString();
}

function row(
  rank: number,
  team: string,
  teamLogo: string | null,
  played: number,
  won: number,
  drawn: number,
  lost: number,
  goalsFor: number,
  goalsAgainst: number,
  form: LeagueFormResult[],
): LeagueStandingRow {
  return {
    rank,
    team,
    teamLogo,
    played,
    won,
    drawn,
    lost,
    goalsFor,
    goalsAgainst,
    points: won * 3 + drawn,
    form,
  };
}

function fixture(
  id: string,
  homeTeam: string,
  awayTeam: string,
  homeLogo: string | null,
  awayLogo: string | null,
  hours: number,
  matchday: string,
): LeagueFixture {
  return {
    id,
    homeTeam,
    awayTeam,
    homeLogo,
    awayLogo,
    kickoffAt: hoursFromNow(hours),
    matchday,
  };
}

